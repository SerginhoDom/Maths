function [ z ] = multiple( z1, z2 )
z(1) = (z1(1) * z2(1)) - (z1(2) * z2(2) + z1(3) * z2(3) + z1(4) * z2(4));
z(2) = (z1(1) * z2(2)) + (z2(1) * z1(2)) + (z1(3)*z2(4) - z1(4)*z2(3));
z(3) = (z1(1) * z2(3)) + (z2(1) * z1(3)) + (z1(4)*z2(2) - z1(2)*z2(4));
z(4) = (z1(1) * z2(4)) + (z2(1) * z1(4)) + (z1(2)*z2(3) - z1(3)*z2(2));
end

